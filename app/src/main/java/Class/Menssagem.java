package Class;

public class Menssagem {
    private int idOrig;
    private int idCreat;
    private int idDest;
    private String titulo, desc;

    public int getIdOrig() {
        return idOrig;
    }

    public void setIdOrig(int idOrig) {
        this.idOrig = idOrig;
    }

    public int getIdCreat() {
        return idCreat;
    }

    public void setIdCreat(int idCreat) {
        this.idCreat = idCreat;
    }

    public int getIdDest() {
        return idDest;
    }

    public void setIdDest(int idDest) {
        this.idDest = idDest;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
